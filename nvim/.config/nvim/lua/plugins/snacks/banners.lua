-- Get all banners from a category
---@param category "logo"
---@return table
function bannars.get_all(category)
    return data[category]
end

-- Get a random banner from a category
---@param category "logo"
---@return string
function bannars.get_random(category)
    local banners = data[category]
    local keys = {}
    for k, _ in pairs(banners) do
        table.insert(keys, k)
    end
    math.randomseed(os.time()) -- Ensure true randomness
    local random_key = keys[math.random(#keys)]
    return banners[random_key]
end

---Get a specific banner
---@param category "logo"
---@return table
function bannars.get(category)
    return data[category]
end

